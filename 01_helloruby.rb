#字串符对象
print("Hello, Ruby.\n")

#方法+参数
print("Hello, RUBY!\n")

#换行符
print("Hello,\nRuby\n!\n")

print("Hello,
Ruby
!
")

#内双引号
print("Hello,\"Ruby\".\n")

#斜杠
print("Hello\\Ruby!\n")

#单引号——原封不动的输出
print('Hello, \nRuby\n!\n')

#内单引号
print('Hello, \\ \'Ruby\'.')

#多个字串符,方法调用，省略（）
print "Hello, Ruby.\n"

print "Hello,", "Ruby", "." "\n"

# print ("Hello,"), ("Ruby"), (".") ("\n") 错的

print "Hello,"
print "Ruby"
print "."
print "\n"
